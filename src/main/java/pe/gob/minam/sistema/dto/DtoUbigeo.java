package pe.gob.minam.sistema.dto;

/**
 *
 * @author jmarinc
 */
public class DtoUbigeo {
    
    private String codigoDep;
    private String codigoPro;
    private String codigoDis;
    private String nombre;
    private String valor;

    public String getCodigoDep() {
        return codigoDep;
    }

    public void setCodigoDep(String codigoDep) {
        this.codigoDep = codigoDep;
    }

    public String getCodigoPro() {
        return codigoPro;
    }

    public void setCodigoPro(String codigoPro) {
        this.codigoPro = codigoPro;
    }

    public String getCodigoDis() {
        return codigoDis;
    }

    public void setCodigoDis(String codigoDis) {
        this.codigoDis = codigoDis;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
    
    
    
}
